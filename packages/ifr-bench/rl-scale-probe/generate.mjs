/**
 * Emit a ReactLynx App that mirrors the Vue sfc-probe content scene:
 * feed header + N literal cards (same bindings / element density).
 * Unrolled JSX so Snapshot IFR can bake the first screen the same way
 * Vue IFR/ET bakes the SFC probe.
 *
 *   node generate.mjs [nCards=125]
 */
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const _dirname = path.dirname(fileURLToPath(import.meta.url));

export function generateReactProbe(nCards = 125) {
  const cards = [];
  for (let i = 0; i < nCards; i++) {
    cards.push(`      <view className={state.cls[${i % 3}]}>
        <image className="card-icon" src="asset://c.png" />
        <view className="card-body">
          <text className="card-title">{state.titles[${i % 5}]}</text>
          <text className="card-desc">Static description text for the card body</text>
        </view>
        <view className="card-footer">
          <text className="card-meta">{state.metas[${i % 5}]}</text>
          <text className="card-cta">Open</text>
        </view>
      </view>`);
  }

  const app = `// GENERATED by generate.mjs — do not edit by hand.
import { useState } from '@lynx-js/react'

const TITLES = ['Card title 0', 'Card title 1', 'Card title 2', 'Card title 3', 'Card title 4']
const METAS = ['meta-0', 'meta-1', 'meta-2', 'meta-3', 'meta-4']
const CLS = ['card variant-0', 'card variant-1', 'card variant-2']

export function App() {
  const [feedTitle, setFeedTitle] = useState('Today')
  const state = { titles: TITLES, metas: METAS, cls: CLS }
  return (
    <view className="feed" bindtap={() => setFeedTitle('Tapped')}>
      <view className="feed-header">
        <text className="feed-title">{feedTitle}</text>
      </view>
      <view className="feed-list">
${cards.join('\n')}
      </view>
    </view>
  )
}
`;

  const index = `// GENERATED by generate.mjs — do not edit by hand.
import { root } from '@lynx-js/react'
import { App } from './App.jsx'
root.render(<App />)
`;

  const css = `/* GENERATED — matches sfc-probe card styles */
.feed { flex: 1; display: flex; flex-direction: column; background-color: #f5f5f5; }
.feed-header { padding: 12px 16px; }
.feed-title { font-size: 18px; font-weight: bold; color: #111; }
.feed-list { display: flex; flex-direction: column; }
.card { display: flex; flex-direction: row; margin: 4px 12px; padding: 8px; background-color: #fff; border-radius: 8px; }
.variant-0 { border-left-width: 2px; border-left-color: #42b883; }
.variant-1 { border-left-width: 2px; border-left-color: #35495e; }
.variant-2 { border-left-width: 2px; border-left-color: #ff7043; }
.card-icon { width: 32px; height: 32px; }
.card-body { flex: 1; display: flex; flex-direction: column; margin: 0 8px; }
.card-title { font-size: 14px; color: #222; }
.card-desc { font-size: 11px; color: #888; }
.card-footer { display: flex; flex-direction: column; align-items: flex-end; }
.card-meta { font-size: 10px; color: #aaa; }
.card-cta { font-size: 12px; color: #42b883; }
`;

  const srcDir = path.join(_dirname, 'src');
  fs.mkdirSync(srcDir, { recursive: true });
  fs.writeFileSync(path.join(srcDir, 'App.jsx'), app);
  fs.writeFileSync(path.join(srcDir, 'index.tsx'), index);
  fs.writeFileSync(path.join(srcDir, 'App.css'), css);
  // ensure CSS is imported
  fs.writeFileSync(
    path.join(srcDir, 'index.tsx'),
    `// GENERATED by generate.mjs — do not edit by hand.
import { root } from '@lynx-js/react'
import { App } from './App.jsx'
import './App.css'
root.render(<App />)
`,
  );

  return { nCards, elements: 4 + nCards * 8 };
}

if (process.argv[1] === fileURLToPath(import.meta.url)) {
  const n = Number(process.argv[2] ?? 125);
  const out = generateReactProbe(n);
  console.log(`generated react probe: ${out.nCards} cards, ~${out.elements} elements`);
}

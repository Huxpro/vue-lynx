// Content workload generator — the same-source SFC used for first-screen (FCP)
// scaling. Adapted from ifr-bench/sfc-probe/generate.mjs so the unified harness
// owns one copy. A feed header + N literal cards, each with a dynamic class and
// two dynamic text bindings against reactive state. Using ONE generated source
// for every matrix cell removes example content as a variable — only renderer
// mode and IFR/ET flags differ.
//
// Element geometry MUST match core/scale.mjs: elements = CONTENT_CHROME(4) +
// nCards * CONTENT_PER_CARD(8).

import fs from 'node:fs';
import path from 'node:path';

export function generateContentApp(targetSrcDir, mode, nCards = 125) {
  if (mode !== 'vapor' && mode !== 'vdom') {
    throw new Error(`mode must be vapor|vdom, got ${mode}`);
  }

  const cards = [];
  for (let i = 0; i < nCards; i++) {
    cards.push(`      <view :class="state.cls[${i % 3}]">
        <image class="card-icon" src="asset://c.png" />
        <view class="card-body">
          <text class="card-title">{{ state.titles[${i % 5}] }}</text>
          <text class="card-desc">Static description text for the card body</text>
        </view>
        <view class="card-footer">
          <text class="card-meta">{{ state.metas[${i % 5}] }}</text>
          <text class="card-cta">Open</text>
        </view>
      </view>`);
  }

  const scriptAttrs = mode === 'vapor' ? ' setup vapor' : ' setup';
  const app = `<script${scriptAttrs} lang="ts">
// GENERATED by core/workloads/content.mjs — do not edit by hand.
import { reactive } from 'vue';

const state = reactive({
  feedTitle: 'Today',
  titles: ['Card title 0', 'Card title 1', 'Card title 2', 'Card title 3', 'Card title 4'],
  metas: ['meta-0', 'meta-1', 'meta-2', 'meta-3', 'meta-4'],
  cls: ['card variant-0', 'card variant-1', 'card variant-2'],
});

function onTap() {
  state.feedTitle = 'Tapped';
}
</script>

<template>
  <view class="feed" @tap="onTap">
    <view class="feed-header">
      <text class="feed-title">{{ state.feedTitle }}</text>
    </view>
    <view class="feed-list">
${cards.join('\n')}
    </view>
  </view>
</template>

<style scoped>
.feed { flex: 1; display: flex; flex-direction: column; background-color: #f5f5f5; }
.feed-header { padding: 12px 16px; }
.feed-title { font-size: 18px; font-weight: bold; color: #111; }
.feed-list { display: flex; flex-direction: column; }
.card { display: flex; flex-direction: row; margin: 4px 12px; padding: 8px; background-color: #fff; border-radius: 8px; }
.variant-0 { border-left-width: 2px; border-left-color: #42b883; }
.variant-1 { border-left-width: 2px; border-left-color: #35495e; }
.variant-2 { border-left-width: 2px; border-left-color: #ff7043; }
.card-icon { width: 32px; height: 32px; }
.card-body { flex: 1; display: flex; flex-direction: column; margin: 0 8px; }
.card-title { font-size: 14px; color: #222; }
.card-desc { font-size: 11px; color: #888; }
.card-footer { display: flex; flex-direction: column; align-items: flex-end; }
.card-meta { font-size: 10px; color: #aaa; }
.card-cta { font-size: 12px; color: #42b883; }
</style>
`;

  const index = `// GENERATED by core/workloads/content.mjs — do not edit by hand.
import { createApp } from 'vue';
import App from './App.vue';
const app = createApp(App);
app.mount();
`;

  fs.mkdirSync(targetSrcDir, { recursive: true });
  fs.writeFileSync(path.join(targetSrcDir, 'App.vue'), app);
  fs.writeFileSync(path.join(targetSrcDir, 'index.ts'), index);
  fs.writeFileSync(path.join(targetSrcDir, 'shims-vue.d.ts'), `declare module '*.vue';\n`);
  return { nCards, elements: 4 + nCards * 8 };
}

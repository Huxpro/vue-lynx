// React (ReactLynx) content workload generator — the cross-framework
// counterpart of core/workloads/content.mjs. Renders N cards on first frame
// with the SAME element geometry (8 elements/card + 4 chrome) so ReactLynx
// FCP lands on the same element axis as the Vue cells. ReactLynx ships
// snapshot + IFR always-on (no flag variance), so there is a single react
// content cell.
//
// Emits into the existing apps/ui-react toolchain (reuses its installed
// ReactLynx rsbuild plugin) as ContentApp.tsx + index-content.tsx.

import fs from 'node:fs';
import path from 'node:path';

export function generateReactContentApp(uiReactSrcDir, nCards = 125) {
  const app = `// GENERATED by core/workloads/content-react.mjs — do not edit by hand.
import './App.css';

const CLS = ['card variant-0', 'card variant-1', 'card variant-2'];
const TITLES = ['Card title 0', 'Card title 1', 'Card title 2', 'Card title 3', 'Card title 4'];
const METAS = ['meta-0', 'meta-1', 'meta-2', 'meta-3', 'meta-4'];
const N = ${nCards};
const cards = Array.from({ length: N }, (_, i) => i);

export function ContentApp() {
  return (
    <view className="feed">
      <view className="feed-header">
        <text className="feed-title">Today</text>
      </view>
      <view className="feed-list">
        {cards.map((i) => (
          <view key={i} className={CLS[i % 3]}>
            <image className="card-icon" src="asset://c.png" />
            <view className="card-body">
              <text className="card-title">{TITLES[i % 5]}</text>
              <text className="card-desc">Static description text for the card body</text>
            </view>
            <view className="card-footer">
              <text className="card-meta">{METAS[i % 5]}</text>
              <text className="card-cta">Open</text>
            </view>
          </view>
        ))}
      </view>
    </view>
  );
}
`;
  const index = `// GENERATED by core/workloads/content-react.mjs — do not edit by hand.
import { root } from '@lynx-js/react';
import { ContentApp } from './ContentApp';
root.render(<ContentApp />);
`;
  fs.mkdirSync(uiReactSrcDir, { recursive: true });
  fs.writeFileSync(path.join(uiReactSrcDir, 'ContentApp.tsx'), app);
  fs.writeFileSync(path.join(uiReactSrcDir, 'index-content.tsx'), index);
  return { nCards, elements: 4 + nCards * 8 };
}

import { defineConfig } from '@lynx-js/rspeedy';
import { pluginVueLynx } from 'vue-lynx/plugin';

// Content workload (first-screen FCP scaling), matrix-driven. The unified
// runner (harness/unified-content.mjs) sets env per cell so one source builds
// every four-axis architecture:
//   BENCH_VAPOR=0|1       renderer (vdom | vapor)
//   BENCH_IFR=0|1         enableIFR
//   BENCH_ET=0|1          enableElementTemplates (vdom only)
//   BENCH_NAMING=dense    templateNaming (vapor; default sparse)
//   BENCH_STAGING=engine|code  templateStaging (vapor; engine = stub on web,
//                         code = +b:c build-time create(), #337)
//   BENCH_DELIVERY=bundle templateDelivery (vapor +b!, #338)
//   BENCH_IFRPAINT=engine-et  ifrPaint (vapor; stub fallback on web)
// Flag semantics mirror the base (#327) ui-vdom/ui-vapor configs exactly.
// src/App.vue + src/index.ts are generated by core/workloads/content.mjs.
const vapor = process.env.BENCH_VAPOR === '1';
const enableIFR = process.env.BENCH_IFR === '1';
const templateNaming = process.env.BENCH_NAMING === 'dense' ? ('dense' as const) : ('sparse' as const);
const templateStaging = process.env.BENCH_STAGING === 'engine'
  ? ('engine' as const)
  : process.env.BENCH_STAGING === 'code'
  ? ('code' as const)
  : undefined;
const templateDelivery = process.env.BENCH_DELIVERY === 'bundle' ? ('bundle' as const) : undefined;
const ifrPaint = process.env.BENCH_IFRPAINT === 'engine-et' ? ('engine-et' as const) : undefined;

export default defineConfig({
  environments: {
    web: {},
    lynx: {},
  },
  source: {
    entry: {
      main: './src/index.ts',
    },
  },
  plugins: [
    pluginVueLynx(
      vapor
        ? {
            optionsApi: false,
            vapor: true,
            enableIFR,
            templateNaming,
            templateStaging,
            templateDelivery,
            ifrPaint,
            enableElementTemplates: false,
          }
        : {
            optionsApi: false,
            enableIFR,
            enableElementTemplates: process.env.BENCH_ET === '1',
          },
    ),
  ],
});
